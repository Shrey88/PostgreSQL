/*
*	EXTRACTING FIELD
*/


/*
*	extracting the year from the actual timestamp
*/
SELECT EXTRACT(YEAR FROM NOW());


/*
*	extracting the month from the actual timestamp
*/
SELECT EXTRACT(MONTH FROM NOW());


/*
*	extracting the day from the actual timestamp
*/
SELECT EXTRACT(DAY FROM NOW());


/*
*	extracting the day of the week from the actual timestamp
*/
SELECT EXTRACT(DOW FROM NOW());


/*
*	extracting the century of the week from the actual timestamp
*/
SELECT EXTRACT(CENTURY FROM NOW());